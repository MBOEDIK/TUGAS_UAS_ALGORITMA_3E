function pilihAlgoritma(algoritma) {
    if (algoritma === "huffman") {
        alert(
            "Kamu memilih Huffman Coding.\n\n" +
            "Algoritma ini digunakan untuk kompresi data."
        );
    } else if (algoritma === "dijkstra") {
        alert(
            "Kamu memilih Algoritma Dijkstra.\n\n" +
            "Algoritma ini digunakan untuk mencari jalur terpendek."
        );
    }
}
